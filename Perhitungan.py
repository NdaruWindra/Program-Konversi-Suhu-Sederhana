Nilai_a = int(input("Masukan Nilai A : "))
Nilai_b = int(input("Masukan Nilai B : "))

tambah = Nilai_a + Nilai_b
kurang = Nilai_a - Nilai_b
kali = Nilai_a * Nilai_b
bagi = Nilai_a / Nilai_b
modulus = Nilai_a % Nilai_b

print("Hasil Pertambahann : ", tambah)
print("Hasil Pengurangan  : ", kurang)
print("Hasil Perkalian    : ", kali)
print("Hasil Pembagian    : ", bagi)
print("Hasil Modulus      : ", modulus)
